                <h2 class="title">Wat is de arbowet?</h2>
                <p>Het is belangrijk dat ale werknemers veilig en gezond kunnen werken. Om dit te kunnen waarborgen, is de Arbeidsomstandighedenwet (arbowet)
                in het leven geroepen.Werkgevers en werknemers zijn samen verantwoordelijk voor gezond en veilig werken en ook voor de invulling van deze wet
                binnen de eigen organisatie of branche.<br>
                Kort gezegd staan er in de arbowet dus regels voor werkgevers en werknemers om de gezondheid en veiligheid van werknemers te bevorderen. <br><br>
                In de Arbowet zijn verplichtingen opgenomen waar werkgevers en werknemers zich aan moeten houden. Deze regels
                gelden voor <u>alle</u> plekken waar arbeid wordt verricht. Dus ook voor verenigingen, stichtingen en voor deeltijd- en flexwerkers,
                oproepkrachten en personen met een nulurencontract.
                </p>
                <hr>
                <h2 class="title"> De arbocatalogus</h2>
                <p>Sinds 2007 hebben werkgevers en werknemers meer mogelijkheden gekregen om zelf te bepalen hoe ze de normen van de Arbowet (oftewel de 'doelvoorschriften') in hun ondernemen bereiken.
                    Ook op brancheniveau maken werkgevers en werknemers hier afspraken over. <br>
                    Deze afspraken worden vastgelegd in een arbocatalogus. Hier beschrijven werkgevers en werknemers op eigen initiatief
                    hoe ze zullen voldoen aan de doelvoorschriften van de overheid voor gezond en veilig werken. <br>
                    Om deze in te zien, kunt u <i class="source"> <a href="https://www.arboportaal.nl/externe-bronnen/arbocatalogi">hier</a> klikken</i>.<br><br>
                </p>
                <hr>
                <h2 class="title">Kaderwet</h2>
                <p>De Arbowet is een kaderwet. Dat betekend dat er geen regels in staan over contrete risico's, maar algemene bepalingen
                   over het arbobeleid binnen bedrijven. Deze zijn verder uitgewerkt in het Arbobesluit en de Arboregeling.<br>
                   De wetgeving omtrent arbeidsomstandigheden is ingedeeld in drie niveaus: de Arbowet, het Arbobesluit en de Arboregeling.</p>
                        <ul>
                    <li> <strong>de Arbowet:</strong><br>
                        Dit vormt de basis voor de arbowetgeving. Hierin staan de algemene bepalingen die gelden voor alle plekken waar arbeid wordt verricht<br>
                        <i class="source"><a href="https://wetten.overheid.nl/BWBR0010346/2020-12-01">Hier</a></i> kunt u de volledige tekst van de Arbowet vinden.
                    </li>
                    <li> <strong>het Arbobesluit:</strong><br>
                        Het Arbobesluit is een uitwerking van de Arbowet. Hierin staan regels waar de werkgever en werknemer zich aan moeten houden
                        om arbeidsrisico's tegen te gaan. <br>
                        Hier staan ook specifieke regels in voor een aantal sectoren en categorieën werknemers.<br>
                        <i class="source"><a href="https://wetten.overheid.nl/BWBR0008498/2020-07-01">Hier</a></i> kunt u de volledige tekst van het Arbobesluit vinden.
                    </li>
                    <li> <strong> de Arboregeling:</strong> <br>
                        De Arboregeling is een verdere uitwerking van het Arbobesluit. Het gaat hierbij om <u>gedetailleerde</u> voorschriften.<br>
                        Hier staan bijvoorbeeld de eisen waar de arbeidsmiddelen aan moeten voldoen, of hoe een arbodienst (deze partij helpt werkgevers en werknemers
                        bij het opstellen en uitvoeren van een arbeidsomstandigheden- en verzuimbeleid. De wettelijke arboverplichtingen vereisen een specifieke deskundigheid)
                        zijn wettelijke taken exact moet uitvoeren. Ook deze regels zijn verplicht voor werkgever en werknemer.<br>
                        <i class="source"><a href="https://wetten.overheid.nl/BWBR0008587/2021-01-01">Hier</a></i> kunt u de volledige tekst van de Arboregeling vinden.
                    </li>
                    </ul>
